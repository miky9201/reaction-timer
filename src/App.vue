<template>
  <h1>Ninja Reaction Timer</h1>
  <p>{{ delay }}</p>
  <button @click="start">Play</button>
</template>

<script>
export default {
  name: "App",
  components: {},
  data() {
    return {
      isPlaying: false,
      delay: null,
    };
  },
  methods: {
    start() {
      this.isPlaying = true;
      this.delay = 2000 + Math.random() * 5000;
    },
  },
};
</script>

